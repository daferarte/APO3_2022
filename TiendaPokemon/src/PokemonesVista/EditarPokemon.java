    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DAOpokemones pokCont = PokemonesController.getInstance();
        String codPok=TexFieCodPok.getText();
        String nomPok=TexFieNomPok.getText();
        String tipPok="";
        if(cH_Agua.isSelected())
            tipPok+="Agua ";
        if(cH_Electrico.isSelected())
            tipPok+="Electrico ";
        if(cH_Fuego.isSelected())
            tipPok+="Fuego ";
        if(cH_Planta.isSelected())
            tipPok+="Planta ";
        String sexPok="";
        if(rD_Ma.isSelected()){
            sexPok="Macho";
        }else{
            sexPok="Hembra";
        }
        String varPok="";
        if(cH_Variocolor.isSelected()){
            varPok="Si";
        }else{
            varPok="No";
        }
        String estPok=spiEst.getValue().toString();
        String[] pokemon={codPok,nomPok,tipPok,sexPok,varPok,estPok};
        if(pokCont.ActualizarPokemon(pokemon))
            JOptionPane.showMessageDialog(this, "ACtualizaciÃ³n Exitosa");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    

